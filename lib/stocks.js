export function generateMock(basePrice, days=260) {
  const data=[]; let price=basePrice;
  const end=new Date("2026-05-30");
  for(let i=days;i>=0;i--){
    const date=new Date(end); date.setDate(date.getDate()-i);
    if(date.getDay()===0||date.getDay()===6) continue;
    const noise=(Math.random()-0.49)*price*0.022;
    const open=price, close=Math.max(price*0.92,price+noise+price*0.0003);
    const high=Math.max(open,close)*(1+Math.random()*0.010);
    const low=Math.min(open,close)*(1-Math.random()*0.010);
    data.push({date:date.toISOString().slice(0,10),open:+open.toFixed(2),high:+high.toFixed(2),low:+low.toFixed(2),close:+close.toFixed(2),volume:Math.floor(Math.random()*8e6+200000)});
    price=close;
  }
  return data;
}
